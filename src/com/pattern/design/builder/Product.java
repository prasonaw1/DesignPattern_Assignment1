package com.pattern.design.builder;

public class Product {
	
	private int pId;
	private String pName;
	private String pDescription;
	private int pRating;
	private String pReviews;
	private String pQuestionAnswer;
	private int pQuantity;
	public int getpId() {
		return pId;
	}
	public void setpId(int pId) {
		this.pId = pId;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getpDescription() {
		return pDescription;
	}
	public void setpDescription(String pDescription) {
		this.pDescription = pDescription;
	}
	public int getpRating() {
		return pRating;
	}
	public void setpRating(int pRating) {
		this.pRating = pRating;
	}
	public String getpReviews() {
		return pReviews;
	}
	public void setpReviews(String pReviews) {
		this.pReviews = pReviews;
	}
	public String getpQuestionAnswer() {
		return pQuestionAnswer;
	}
	public void setpQuestionAnswer(String pQuestionAnswer) {
		this.pQuestionAnswer = pQuestionAnswer;
	}
	public int getpQuantity() {
		return pQuantity;
	}
	public void setpQuantity(int pQuantity) {
		this.pQuantity = pQuantity;
	}
	
	

}
